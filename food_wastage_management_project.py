import sqlite3
import pandas as pd

conn = sqlite3.connect("food_wastage.db")

providers = pd.read_csv("providers_data.csv")
receivers = pd.read_csv("receivers_data.csv")
food = pd.read_csv("food_listings_data.csv")
claims = pd.read_csv("claims_data.csv")

providers.to_sql("providers", conn, if_exists="replace", index=False)
receivers.to_sql("receivers", conn, if_exists="replace", index=False)
food.to_sql("food_listings", conn, if_exists="replace", index=False)
claims.to_sql("claims", conn, if_exists="replace", index=False)

print("Database ready!")

# Query 1 - How many providers and receivers in each city?
q1 = """
SELECT p.City,
       COUNT(DISTINCT p.Provider_ID) as Total_Providers,
       COUNT(DISTINCT r.Receiver_ID) as Total_Receivers
FROM providers p
LEFT JOIN receivers r ON p.City = r.City
GROUP BY p.City
ORDER BY Total_Providers DESC
"""
df1 = pd.read_sql_query(q1, conn)
print("Providers & Receivers per City:-")
print(df1)

# Query 2 - Which provider type contributes most food?
q2 = """
SELECT Provider_Type, COUNT(*) as Total_Listings,
       SUM(Quantity) as Total_Quantity
FROM food_listings
GROUP BY Provider_Type
ORDER BY Total_Quantity DESC
"""
df2 = pd.read_sql_query(q2, conn)
print("Provider Type Contributions:-")
print(df2)

# Query 3 - Contact info of providers in a specific city
q3 = """
SELECT Name, Type, Address, City, Contact
FROM providers
WHERE City = 'Mumbai'
"""
df3 = pd.read_sql_query(q3, conn)
print("Provider Contact Info by City:-")
print(df3)

# Query 4 - Which receivers claimed the most food?
q4 = """
SELECT r.Name, r.Type, r.City,
       COUNT(c.Claim_ID) as Total_Claims
FROM receivers r
JOIN claims c ON r.Receiver_ID = c.Receiver_ID
GROUP BY r.Receiver_ID
ORDER BY Total_Claims DESC
LIMIT 10
"""
df4 = pd.read_sql_query(q4, conn)
print("Top Receivers by Claims:-")
print(df4)

# Query 5 - Total quantity of food available
