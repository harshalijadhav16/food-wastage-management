# Query 9 - Which provider had highest successful claims?
q9 = """
SELECT p.Name, p.Type, p.City,
       COUNT(c.Claim_ID) as Successful_Claims
FROM providers p
JOIN food_listings f ON p.Provider_ID = f.Provider_ID
JOIN claims c ON f.Food_ID = c.Food_ID
WHERE c.Status = 'Completed'
GROUP BY p.Provider_ID
ORDER BY Successful_Claims DESC
LIMIT 10
"""
df9 = pd.read_sql_query(q9, conn)
print("Top Providers by Successful Claims:-")
print(df9)

# Query 10 - Percentage of completed vs pending vs cancelled?
q10 = """
SELECT Status,
       COUNT(*) as Total,
       ROUND(COUNT(*) * 100.0 / (SELECT COUNT(*) FROM claims), 2) as Percentage
FROM claims
GROUP BY Status
"""
df10 = pd.read_sql_query(q10, conn)
print("Claim Status Percentage:-")
print(df10)

# Query 11 - Average quantity claimed per receiver?
